package Ejercicios;

public class Perro extends Mamifero{
   private String raza;
   private String chip;
   private String tipo;
public String getRaza() {
	return raza;
}
public void setRaza(String raza) {
	this.raza = raza;
}
public String getChip() {
	return chip;
}
public void setChip(String chip) {
	this.chip = chip;
}
public String getTipo() {
	return tipo;
}
public void setTipo(String tipo) {
	this.tipo = tipo;
}
}
